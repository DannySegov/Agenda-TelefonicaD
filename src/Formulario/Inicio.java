    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        //De esta forma le decimos al programa que datos queremos agregar a la tabla
        Object o[]=null;
        for (int i = 0; i < Agenda.size(); i++){
            Contactos c = (Contactos) Agenda.get(i);
            M.addRow(o);     
            M.setValueAt(c.getNombre(), i, 0);//El número que se encuentra después de la i es el número de la columna
            M.setValueAt(c.getDirección(), i, 1);
            M.setValueAt(c.getEdad(), i, 2);
            M.setValueAt(c.getSexo(), i, 3); 
            M.setValueAt(c.getTeléfono(), i, 4);
            M.setValueAt(c.getCP(), i, 5);
        }
    }//GEN-LAST:event_btnMostrarActionPerformed


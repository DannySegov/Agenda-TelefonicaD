    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String n = txtNombre.getText(); //Aqui lo que hacemos es recibir en la variable n los datos escritos en el txtNombre
        String d = txtDireccion.getText();
        String e = txtEdad.getText();
        String s = txtSexo.getText();
        String t = txtTelefono.getText();
        String c = txtCP.getText();
        
        if(!n.isEmpty()){
            if(!d.isEmpty()){ 
                if(!e.isEmpty()){
                    if(!s.isEmpty()){
                        if(!t.isEmpty()){
                            if(!c.isEmpty()){
                                
                                Contactos con = new Contactos(n, d, Integer.parseInt(e), s, Integer.parseInt(t), Integer.parseInt(c)); //Las variables e, t y c las convertimos a int con el Integer.parseInt
                                Agenda.add(con);
                                
                                JOptionPane.showMessageDialog(null, "Contacto guardado con éxito");
                                txtNombre.setText(""); //Con esta función limpiamos los campos asignandole un valor vacio 
                                txtDireccion.setText("");
                                txtEdad.setText("");
                                txtSexo.setText("");
                                txtTelefono.setText("");
                                txtCP.setText("");
                               
                            }else{
                                JOptionPane.showMessageDialog(null, "El campo CP está vacio");
                            }
                        }else{
                            JOptionPane.showMessageDialog(null, "El campo teléfono está vacio");
                        }
                    }else{
                         JOptionPane.showMessageDialog(null, "El campo sexo está vacio");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "El campo Edad está vacio");
                }
            }else{
                JOptionPane.showMessageDialog(null, "El campo Dirección está vacio");
            }   
        }else{
            JOptionPane.showMessageDialog(null, "El campo Nombte está vacio");
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

